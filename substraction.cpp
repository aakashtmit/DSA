#include <iostream>

using namespace std;

int main() {
    int num1, num2, difference;

    cout << "Enter first number: ";
    cin >> num1;
    cout << "Enter second number: ";
    cin >> num2;

    // Subtract the two numbers
    difference = num1 - num2;

    cout << "The difference is: " << difference << endl;

    return 0;
}
